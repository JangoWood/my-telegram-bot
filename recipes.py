# Строим обратный словарь синонимов для быстрого поиска
ALIAS_TO_RECIPE = {}
for recipe_key, recipe in RECIPES.items():
    # Добавляем основное название (ключ)
    ALIAS_TO_RECIPE[recipe_key] = recipe
    # Добавляем все синонимы
    for alias in recipe.get("aliases", []):
        ALIAS_TO_RECIPE[alias.lower()] = recipe


def get_recipe(recipe_name):
    """Ищет рецепт по названию или синониму (без учёта регистра)"""
    recipe_name_lower = recipe_name.lower().strip()

    # Сначала ищем по синонимам
    if recipe_name_lower in ALIAS_TO_RECIPE:
        return ALIAS_TO_RECIPE[recipe_name_lower]

    # Затем пробуем частичное совпадение
    for key, recipe in RECIPES.items():
        if key in recipe_name_lower:
            return recipe

    return None


def get_all_recipes():
    """Возвращает список всех рецептов"""
    return list(RECIPES.values())